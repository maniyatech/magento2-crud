namespace ManiyaTech\Crud\Block\Adminhtml\Student\Buttons;

use Magento\Framework\View\Element\UiComponent\Control\ButtonProviderInterface;
use ManiyaTech\Crud\Block\Adminhtml\Student\Buttons\GenericButton;

class DeleteButton extends GenericButton implements ButtonProviderInterface
{
    /**
     * Get Button Data
     *
     * @return array
     */
    public function getButtonData()
    {
        $data = [];
        if ($this->getStudId()) {
            $data = [
                'label' => __('Delete'),
                'class' => 'delete',
                'on_click' => 'deleteConfirm(\'' . __(
                    'Are you sure you want to do this?'
                ) . '\', \'' . $this->getDeleteUrl() . '\')',
                'sort_order' => 20,
            ];
        }
        return $data;
    }

    /**
     * Get URL for delete button
     *
     * @return string
     */
    public function getDeleteUrl()
    {
        return $this->getUrl('*/*/delete', ['stud_id' => $this->getStudId()]);
    }
}
